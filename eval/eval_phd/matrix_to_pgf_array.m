function pgf_array = matrix_to_pgf_array(matrix)
    %MATRIX_TO_PGF_ARRAY
    pgf_array = "{";

    for row = matrix'
        pgf_array = strcat( ...
            pgf_array, ...
            "{", sprintf('%d,', row(1:end - 1)), ...
            num2str(row(end)), ...
            "}, " ...
        );
    end

    pgf_array = strcat(pgf_array{1}(1:end - 2), "}");
end
